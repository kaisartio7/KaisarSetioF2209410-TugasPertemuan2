public class Tugas13PBO {
    public static void main(String[] args) {
        // Karakter yang akan dihitung kode ASCII-nya
        char charA = 'A';
        char charB = 'B';
        
        // Menghitung kode ASCII
        int asciiA = (int) charA;
        int asciiB = (int) charB;
        
        // Menjumlahkan kode ASCII
        int totalAscii = asciiA + asciiB;
        
        // Menampilkan hasil
        System.out.println("Kode ASCII 'A': " + asciiA);
        System.out.println("Kode ASCII 'B': " + asciiB);
        System.out.println("Jumlah kode ASCII 'A' dan 'B': " + totalAscii);
    }
}
 

